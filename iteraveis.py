# Iteráveis.
# passar por dentro do for diversas vezes

# Iterável de número inteiro
for item in range (10):
    print(item)

# Iterável de string
for letra in 'programação':
    print(letra)

# Iterável da lista
for item1 in [2,3,4,5]:
    print(item1)

for nomes_diversos in ['Marcos', 'Vinicius', 'Montezi', 10]:
    print(nomes_diversos)

nomes = ['1', '2', '3', '4', '5']
for nome in nomes:
    print(nome)


